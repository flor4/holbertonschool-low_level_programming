#include "3-calc.h"

/**
 * op_add - addition two integers.
 *
 * @a: the integer to addition with.
 * @b: the integer to addition to.
 *
 * Return: the sum of.
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of two integers.
 *
 * @a : integer.
 * @b : integer.
 *
 * Return: the subtraction of.
 *
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of two integers.
 *
 * @a : integer.
 * @b : integer.
 *
 * Return: the multiplication of.
 *
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two integers.
 *
 * @a : integer.
 * @b : integer.
 *
 * Return: the division of.
 *
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of two integers.
 *
 * @a : integer.
 * @b : integer.
 *
 * Return: the modulo of.
 *
 */

int op_mod(int a, int b)
{
	return (a % b);
}

